class Solution {
public:
    Node* connect(Node* root) {
        if(!root) return NULL;
        Node *start=root;
        while(start){
            Node *current=start;
            while(current){
                if(current->left){
                    current->left->next=current->right;
                }
                if(current->right&&current->next){
                    current->right->next=current->next->left;
                }
                current=current->next;
            }
            start=start->left;
        }
        return root;
    }
};